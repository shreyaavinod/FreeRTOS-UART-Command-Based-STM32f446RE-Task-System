void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
  if(xQueueIsQueueFullFromISR(inputqueue)==pdFALSE)
  {
	  xQueueSendFromISR(inputqueue,(void *) &userdata,NULL);
  }else
  {
	  if(userdata=='\n')
	  {
		  xQueueReceiveFromISR(inputqueue, &userdata, NULL);
		  xQueueSendFromISR(inputqueue,&userdata,NULL);
	  }
  }
  if (userdata=='\n')
  {
	  xTaskNotifyFromISR(cmdhandlertask,0,eNoAction,NULL);
  }

  // AGAIN ENABLE THE HAL UART RECEIVE

  HAL_UART_Receive_IT(&huart2,&userdata,1);
}



